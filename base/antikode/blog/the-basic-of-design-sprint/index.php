        <p>Let’s say you want to create a product. You spend an enormous time building its prototype, launch it, and learn from the result to improve the product. The whole process seems like a good idea. But, what if after you launch the product, you realize that your idea is not good enough? You’ve invested a lot of time crafting an idea,you certainly don’t want spend more time and money to redo the whole process.</p>
        <p>This is where a design sprint can help you avoid such situation. Design sprint is a framework crated by Google Ventures in 2010. It proposed a shortcut to the full-circle design process. This method has even helped Google improve Google Hangouts and Gmail.</p>

        <h3>What is design sprint? What is the goal?</h3>
        <p>According to the Google’s Design Sprint Kit official page, a design sprint is a five-phase framework that helps answer critical business questions through rapid prototyping and user testing. It’s basically a way to solve design problems quickly usually within 5 days. The aim is to encourage user-centered thinking, spark innovation, and align your team under the same vision. This way, you’ll get to launch product faster.</p>

        <h3>What are the stages of design sprint?</h3>
        <p>Design sprint consists of five phases, which usually held for five days (one day for each phase):</p>

        <h4><em>Understand</em></h4>
        <p>To come up with good solution, first you need to understand the problem. The first phase is all about gathering information about the business, the problem, and the user’s needs. The aim is to create a shared vision for the team to make them understand what they’re working towards.</p>

        <h4><em>Sketch</em></h4>
        <p>Now that you and your team understand the problem, phase two requires you to explore all possible solutions by sketching them. Sketching helps you transform an abstract idea into something concrete. This way, everyone can evaluate every sketch and ideas.</p>

        <h4><em>Decide</em></h4>
        <p>In phase two, you will have many sketch versions of your solution, and now it’s time to review them. You need to filter them down and choose the best solutions that will help achieve your goal. Through this review, your team should come up with a conclusion about what kind of prototype they want to build.</p>

        <h4><em>Prototype</em></h4>
        <p>In phase four, you’re required to create a prototype and test it to target users. Remember, you’re not building a fully-functioning product so you only need to create a prototype that allow you to get feedback from your users.</p>

        <h4><em>Validate</em></h4>
        <p>Now you’re ready to test your prototype with target users. Show your prototype to a user and do a usability testing. During this phase, you’ll receive a lot of feedback, so make sure you listen to it, take notes, and use it to improve your product.</p>

        <hr class="section-divider">

        <h3>When is the right time to run a design sprint?</h3>
        <p>You can run a design sprint anytime of the design process. However, according to Adobe.com, there are few cases when the design sprint is particularly effective:</p>

        <ul>
          <li>- When you’re stuck with design decisions</li>
          <li>- When you have a tight deadline and want to get things done in short period of time</li>
          <li>- When there are big challenges need to be solved immediately</li>
        </ul>

        <br>

        <p>Design sprint allows you to solve design problems in a quick time. At the end of the design sprint process, you will have users feedback that you can use to <strong>improve your product</strong>.</p>

