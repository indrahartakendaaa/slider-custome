        <p>Usability test goes by many names: design testing, product testing, user testing, etc. Whatever name you choose to call it, they all have the same goal, which is to test your designs with target users to understand their concerns and usability issues. Once you do that, you’ll have better chance to come up with the right solutions.</p>
        <p>However, please keep in mind that a usability test is not just about stunning visuals. It’s more about solving problems efficiently. A usability test will give you valuable insights into what works and what doesn’t. To make sure you have a successful usability test, there are several steps you need to do.</p>

        <h3>Define your goals</h3>
        <p>First thing first, define a goal of your usability test. Without a clear goal in mind, you don’t know what you’re going to achieve. It’s like you drive a car without a specific destination.</p>
        <p>Your goal can be as simple as “which button design works best for increasing website conversion?” or “can users submit form easily?”. However, among all of the questions you have about your product, you shouldn’t use all of those as your goals. Each test should highlight the most relevant issue at the moment. This enables you to really focus so you can have the most accurate results.</p>

        <h3>Make a test plan</h3>
        <p>Once you define the goal, it’s time to come up with a test plan: what product are you going to test and how is it going to be measured. For example, you’re testing an e-commerce website that sells women outfits. There are two main features you want to highlight on the website: search feature based on various categories and checkout methods. Based on this test plan, you can go through the prototype and come up with some questions such as “are the checkout methods intuitive enough for the users?” and “can they search the products easily?”.</p>
        <p>Let’s say there are ten people agreed to become the testing participants. Before the session begins, create a spreadsheet with four columns: column 1 for the names of the participants, columns 2-3 for the two features of the website to be tested, and column 4 for the additional note if a participant struggles using your feature and you need to write down the reason. </p>

        <h3>Find participants and a location</h3>
        <p>Now that you’ve made a test plan, you need to find some participants. Actually, the only requirement is that your participants have not used your product or website before. However, it’s better if you can find participants that represent your product or website’s audience. For example, if you’re making an e-commerce website that sells outfits for younger women, it’s better to test your product on women of age around 18-30.</p>
        <p>Besides participants, you also need to find the right location to conduct the usability test. Book a quiet room so that you can clearly hear what each participant has to say. Also, make sure that the location is located in the strategic area so that participants can easily find it.</p>

        <h3>Run the test</h3>
        <p>It’s time to run the test! The success of your usability test is heavily influenced by your ability to communicate effectively. This is why you need to prepare and make a test plan so that you can practice before. Make sure that your participants are not the ones being tested—it’s the design. Tell them not to worry if they’re making any mistakes because you’re there to learn from their experiences.
</p>
<p>When the test session ends, don’t forget to thank each of your participants for their time. Remind them that the reason you’re doing this usability test is that you really want to optimize your product or website to provide the best user experience possible.</p>

        <hr class="section-divider">

        <p>Besides those tips mentioned, don’t forget to record the participant’s screen during the test. This way, you can review the videos to ensure you didn’t miss anything during the session. Good luck!</p>

