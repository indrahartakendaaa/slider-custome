        <p>Users would like to use an app that is useful, relevant, and valuable. One of the ways to get there is by creating a good UX design. If you can create the good UX designs, users will more likely to enjoy the experience using your app. Here are dos and don’ts of good UX designs that you should know! </p>

        <p>UX, or user experience, is the answer. It’s a process of improving customer satisfaction through the usability, accessibility, and pleasure provided in the interaction with a product. Revolving around the experience of a user and their thought process, UX covers every aspect of your customers’ interaction with your brands, products, and services. It’s what makes them either want to use your product again or avoid it instead.</p>

        <hr class="section-divider">

        <h3>Take the Do’s In Action</h3>

        <h3>1. Before design, do the research</h3>
        <p>It is normal to feel like wanting to start your UX design project immediately. You may don’t want to waste time and just want to jump straight to a drawing board and start designing mockups. However, it is best for you to do the research beforehand. Remember that you are not your users. Therefore, better do proper research that will strongly help you to understand who your users are and what they actually need.</p>

        <h3>2. Take features as a priority</h3>
        <p>Many product designers try to add as many features as possible to make an app more attractive to users. However, this is not the correct way to get users get a good experience with your app. It is such a confusing moment for a first-time user to use an app that has too much going on. For that reason, you better learn from some successful apps that focused and present a limited set of features by prioritizing what’s important.</p>

        <h3>3. Get rid of the clutter</h3>
        <p>Presenting users with too much information is not a wise move to do. Accordingly, to create a good UX design you have to get rid of the clutter. Clutter is terrible on a desktop, and it’s way worse on mobile devices as users have such limited screen space. You essentially need to get rid of every unnecessary thing in your UX design in order to reduce the clutter that will improve comprehension.</p>
        
        <hr class="section-divider">

        <h3>Avoid the Don’ts</h3>

        <h3>1. Stop trying to design the good experience from the first attempt </h3>
        <p>You need to realize that it might take you a while to be able to create the good UX design. It is almost impossible to create the good UX design right from the start. Hence, you need to be patient and simply never stop to try. Even if your design satisfies the initial requirements, you might find a new set of requirements after users testing. For this reason, it is better for you to follow an iterative design approach and test as early as possible. </p>

        <h3>2. Avoid doing the design in isolation</h3>
        <p>It is not too rare to find a product team that has spent a lot of time, like three to five years, to build a product just to discover that the hard work is almost useless. They were building a product that wasn’t what customers wanted. Therefore, you need to understand that to have a great idea about UX design is good, but don’t forget to take customers needs and wants into consideration. You won’t be happy to spend years just for almost nothing to count.</p>

        <h3>3. Try not to ask permissions right at the start</h3>
        <p>Many apps present you a dialog box with a permission request like “allow the app to use your location” right after you downloaded the app. You better don’t do this, unless it is absolutely necessary for the core app experience. It’s because sometimes users won’t give permission because they don’t have any context to make a decision. It is better to ask for permissions in context so that users will grant permissions because it is asked during a relevant task.</p>

        <hr class="section-divider">

        <p>To make the good UX design, you need to meet your audience expectation. Figure out what your audience wants and needs and follow these tips, then you might find a way to create the UX design that is useful, relevant, and valuable.</p>


